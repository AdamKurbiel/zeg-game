const maps = {
    1:[
    "####################",
    "#S.....#..........E#",
    "#.###..#..#######..#",
    "#...#..#.....#.....#",
    "###.#..#####.#.###.#",
    "#...#......#.#...#.#",
    "#.#######..#.###.#.#",
    "#.....H....#.....#.#",
    "#..######..#####.#.#",
    "#................#.#",
    "####################"],

    2:[
    ".............",
    ".............",
    ".............",
    "...#..#......",
    ".............",
    "..#....#.....",
    "...####......"]
};



export function getMap(index){
    return maps[index].map(row => row.split(""));
};

export function Map(){
    this.level = 1;
    this.content = getMap(this.level);
};

Map.prototype.clearRow = function(x,y){
    this.content[y][x] = ".";
}